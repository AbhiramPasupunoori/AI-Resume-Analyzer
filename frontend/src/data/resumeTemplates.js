const RECOMMENDED_TEMPLATE_IDS = new Set(
  Object.values(RECOMMENDED_TEMPLATE_IDS_BY_CATEGORY).flat()
);

export const RESUME_TEMPLATES = BASE_RESUME_TEMPLATES.map((template) => ({
  ...template,
  recommended: RECOMMENDED_TEMPLATE_IDS.has(template.id),
}));

export function getResumeTemplate(templateId) {
  const aliases = { classic: "ats-classic", modern: "modern-edge", ats: "ats-classic" };
  const normalizedId = aliases[templateId] || templateId;
  return (
    RESUME_TEMPLATES.find((template) => template.id === normalizedId) ||
    RESUME_TEMPLATES[0]
  );
}

export default RESUME_TEMPLATES;
